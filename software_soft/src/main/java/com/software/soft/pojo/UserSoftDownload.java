package com.software.soft.pojo;

public class UserSoftDownload {

    private String usdId;

    private String id;

    private String name;

    private String pic;

    private float rate;

    private int isRate;

    public String getUsdId() {
        return usdId;
    }

    public void setUsdId(String usdId) {
        this.usdId = usdId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }

    public float getRate() {
        return rate;
    }

    public void setRate(float rate) {
        this.rate = rate;
    }

    public int getIsRate() {
        return isRate;
    }

    public void setIsRate(int isRate) {
        this.isRate = isRate;
    }
}
